export function getEstacionesHAZMAT(): Estacion[] {
  return estacionesHidalgo.filter(e =>
    e.servicios.some(s => s.toLowerCase().includes('hazmat')) ||
    e.especialidades?.some(esp => esp.toLowerCase().includes('hazmat'))
  );
}

/**
 * Obtiene estaciones con especialidad en rescate minero
 */
export function getEstacionesRescateMinero(): Estacion[] {
  return estacionesHidalgo.filter(e =>
    e.servicios.some(s => s.toLowerCase().includes('mina') || s.toLowerCase().includes('minero'))
  );
}

/**
 * Obtiene estaciones con capacidad de rescate en montaña
 */
export function getEstacionesRescateMontana(): Estacion[] {
  return estacionesHidalgo.filter(e =>
    e.servicios.some(s =>
      s.toLowerCase().includes('montaña') ||
      s.toLowerCase().includes('forestal') ||
      s.toLowerCase().includes('sierra')
    )
  );
}

/**
 * Genera metadatos SEO avanzados para una estación específica
 */
export function generarMetaSEO(estacion: Estacion): {
  title: string;
  description: string;
  keywords: string[];
  canonical: string;
  ogTitle: string;
  ogDescription: string;
} {
  const ciudadSlug = estacion.ciudad.toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/\s+/g, '-');

  let contexto = '';
  let keywordsAdicionales: string[] = [];

  // Contexto específico por zona
  if (estacion.ciudad === 'Pachuca de Soto') {
    contexto = 'capital del estado, La Bella Airosa, cuna del fútbol mexicano';
    keywordsAdicionales = HIDALGO_SEO.keywords.pachuca;
  } else if (estacion.ciudad === 'Mineral del Monte') {
    contexto = 'Pueblo Mágico, Real del Monte, tradición minera, pastes';
    keywordsAdicionales = [...HIDALGO_SEO.keywords.turismo, ...HIDALGO_SEO.keywords.mineria];
  } else if (estacion.ciudad === 'Tula de Allende') {
    contexto = 'corredor industrial, zona arqueológica tolteca, refinería PEMEX';
    keywordsAdicionales = HIDALGO_SEO.keywords.industrial;
  } else if (estacion.ciudad === 'Tulancingo de Bravo') {
    contexto = 'segunda ciudad del estado, industria textil';
    keywordsAdicionales = HIDALGO_SEO.keywords.tulancingo;
  } else if (estacion.ciudad === 'Huasca de Ocampo') {
    contexto = 'Pueblo Mágico, Prismas Basálticos, haciendas coloniales';
    keywordsAdicionales = HIDALGO_SEO.keywords.turismo;
  } else if (estacion.ciudad === 'Mineral del Chico') {
    contexto = 'Pueblo Mágico, Parque Nacional El Chico, escalada';
    keywordsAdicionales = HIDALGO_SEO.keywords.turismo;
  } else if (estacion.ciudad === 'Ixmiquilpan') {
    contexto = 'Valle del Mezquital, balnearios, cultura otomí';
    keywordsAdicionales = HIDALGO_SEO.keywords.mezquital;
  } else if (estacion.ciudad === 'Huejutla de Reyes') {
    contexto = 'Huasteca Hidalguense, cultura nahua';
    keywordsAdicionales = HIDALGO_SEO.keywords.huasteca;
  } else {
    contexto = 'estado de Hidalgo';
    keywordsAdicionales = HIDALGO_SEO.keywords.principales.slice(0, 5);
  }

  const title = `${estacion.nombre} | Bomberos ${estacion.ciudad}, Hidalgo | Tel. ${estacion.telefono}`;
  const description = `${estacion.nombre} en ${estacion.ciudad}, ${contexto}. Teléfono: ${estacion.telefono}. ${estacion.personal} bomberos profesionales, ${estacion.unidades} unidades de emergencia. Servicios 24/7: ${estacion.servicios.slice(0, 3).join(', ')}. Tiempo de respuesta: ${estacion.tiempoRespuestaMinutos || 8} minutos.`;

  return {
    title,
    description,
    keywords: [
      `bomberos ${estacion.ciudad.toLowerCase()}`,
      `bomberos ${ciudadSlug} hidalgo`,
      `estación bomberos ${estacion.ciudad.toLowerCase()}`,
      `emergencias ${estacion.ciudad.toLowerCase()}`,
      `teléfono bomberos ${estacion.ciudad.toLowerCase()}`,
      `${estacion.telefono}`,
      'bomberos hidalgo',
      'emergencias hidalgo',
      'protección civil hidalgo',
      '911 hidalgo',
      ...keywordsAdicionales.slice(0, 8),
    ],
    canonical: `https://bombero.mx/directorio/hidalgo/${estacion.slug}`,
    ogTitle: `${estacion.nombreCorto} | Emergencias ${estacion.ciudad}`,
    ogDescription: `Bomberos de ${estacion.ciudad}, Hidalgo. Tel: ${estacion.telefono}. Servicio 24/7.`,
  };
}

